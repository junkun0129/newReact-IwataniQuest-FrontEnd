type collisionMapType = {
  rowIndex: number;
  columnIndex: number;
};
export const getCollisionArrayByColumn = (
  array: number[],
  columnNum: number,
  collisionNum: number
) => {
  let newArray: number[][] = [];
  for (let i = 0; i < array.length; i += columnNum) {
    newArray.push(array.slice(i, columnNum + i));
  }

  let collisionMap: collisionMapType[] = [];

  newArray.forEach((row, rowIndex) => {
    row.forEach((column, columnIndex) => {
      if (column !== collisionNum) return;
      collisionMap.push({ rowIndex, columnIndex });
    });
  });
  return collisionMap;
};
